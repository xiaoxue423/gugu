<template>
  <div class="g-button-group">
    <slot></slot>
  </div>
</template>

<script>
export default {
  name: "button-group",
  mounted() {
    for(let node of this.$el.children){
      let name = node.nodeName.toLowerCase()
      if(name!== 'button'){
        console.warn(`g-button-group的子元素应该全是g-button,但是你写的是${name}`)
      }
}
  }
}
</script>

